package garageServices;

public class Voiture {

	String matircule;
	String marque;
	Integer prix;
	String nbPlaces;
	public String getMatircule() {
		return matircule;
	}
	public void setMatircule(String matircule) {
		this.matircule = matircule;
	}
	public String getMarque() {
		return marque;
	}
	public void setMarque(String marque) {
		this.marque = marque;
	}
	public Integer getPrix() {
		return prix;
	}
	public void setPrix(Integer prix) {
		this.prix = prix;
	}
	public String getNbPlaces() {
		return nbPlaces;
	}
	public void setNbPlaces(String nbPlaces) {
		this.nbPlaces = nbPlaces;
	}
	
	
	

}
